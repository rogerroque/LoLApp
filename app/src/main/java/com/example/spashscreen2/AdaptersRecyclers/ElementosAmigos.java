package com.example.spashscreen2.AdaptersRecyclers;

public class ElementosAmigos {

    private int imgChat;
    private String nameAmigo;
    private String statusAmigo;
    private String imgUrlAmigo;

    public int getImgChat() {
        return imgChat;
    }

    public void setImgChat(int imgChat) {
        this.imgChat = imgChat;
    }

    public String getNameAmigo() {
        return nameAmigo;
    }

    public void setNameAmigo(String nameAmigo) {
        this.nameAmigo = nameAmigo;
    }

    public String getStatusAmigo() {
        return statusAmigo;
    }

    public void setStatusAmigo(String statusAmigo) {
        this.statusAmigo = statusAmigo;
    }

    public String getImgUrlAmigo() {
        return imgUrlAmigo;
    }

    public void setImgUrlAmigo(String imgUrlAmigo) {
        this.imgUrlAmigo = imgUrlAmigo;
    }
}
